<?php

namespace app\controllers;

use Yii;
use yii\filters\AccessControl;
use app\models\Empresas;
use app\models\EmpresasSearch;
use yii\web\Controller;
use yii\web\NotFoundHttpException;
use yii\filters\VerbFilter;
use \yii\web\Response;
use yii\helpers\Html;
use yii\web\UploadedFile;

/**
 * EmpresasController implements the CRUD actions for Empresas model.
 */
class EmpresasController extends Controller
{
    /**
     * @inheritdoc
     */
    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::className(),
                'only' => ['index', 'view', 'create', 'update', 'delete', 'bulkdelete'],
                'rules' => [
                    [
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::className(),
                'actions' => [
                    'delete' => ['post'],
                    'bulk-delete' => ['post'],
                ],
            ],
        ];
    }

    public function beforeAction($action)
    {   
        if (!Yii::$app->user->isGuest) {
            if ( Yii::$app->user->identity->es_super != 1 ) {
                $this->redirect(['site/index']);
                return false;
            }
        }
        return parent::beforeAction($action);
    }

    /**
     * Lists all Empresas models.
     * @return mixed
     */
    public function actionIndex()
    {    
        $searchModel = new EmpresasSearch();
        $dataProvider = $searchModel->search(Yii::$app->request->queryParams);

        return $this->render('index', [
            'searchModel' => $searchModel,
            'dataProvider' => $dataProvider,
        ]);
    }


    /**
     * Displays a single Empresas model.
     * @param integer $id
     * @return mixed
     */
    public function actionView($id)
    {   
        $request = Yii::$app->request;
        if($request->isAjax){
            Yii::$app->response->format = Response::FORMAT_JSON;
            return [
                    'title'=> "Empresas #".$id,
                    'content'=>$this->renderAjax('view', [
                        'model' => $this->findModel($id),
                    ]),
                    'footer'=> Html::button('Close',['class'=>'btn btn-default pull-left','data-dismiss'=>"modal"]).
                            Html::a('Edit',['update','id'=>$id],['class'=>'btn btn-primary','role'=>'modal-remote'])
                ];    
        }else{
            return $this->render('view', [
                'model' => $this->findModel($id),
            ]);
        }
    }

    /**
     * Creates a new Empresas model.
     * For ajax request will return json object
     * and for non-ajax request if creation is successful, the browser will be redirected to the 'view' page.
     * @return mixed
     */
    public function actionCreate()
    {

        // ***************** GUARDAR FECHA DE AFILIACION AUTOMATICAMENTE ***************
        $request = Yii::$app->request;
        $model = new Empresas();  

        if($request->isAjax){
            /*
            *   Process for ajax request
            */
            // if( $model->load($request->post()) ){
                $image = UploadedFile::getInstance($model, 'comp_logo_pagomedios');
                if( !empty($image) ){
                    $model->logo = Yii::$app->security->generateRandomString().'_'.$image->name;
                    $path = \Yii::getAlias('@webroot') .'/logos_comercios/' . $model->logo;
                    $image->saveAs($path);
                }
            // }
            $model->fecha_afiliacion = date('Y-m-d H:i:s');
            Yii::$app->response->format = Response::FORMAT_JSON;
            if($request->isGet){
                return [
                    'title'=> "Create new Empresas",
                    'content'=>$this->renderAjax('create', [
                        'model' => $model,
                    ]),
                    'footer'=> Html::button('Close',['class'=>'btn btn-default pull-left','data-dismiss'=>"modal"]).
